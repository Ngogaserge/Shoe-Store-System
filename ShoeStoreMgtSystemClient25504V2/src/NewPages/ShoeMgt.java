    private void addTableRowDataShoe(){
        try {
            tableModel.setRowCount(0);
            Registry thereg = LocateRegistry.getRegistry("127.0.0.1", 6000);
            ShoeService service = (ShoeService) thereg.lookup("shoe");
            List<Shoe> shoes = service.retrieveShoe();
            for(Shoe theshoe: shoes){
                tableModel.addRow(new Object[]{
                    theshoe.getShoeId(),
                    theshoe.getShoeName(),
                    theshoe.getBrand(),
                    theshoe.getShoeSize(),
                    theshoe.getPrice()
                        
//                thecustomers.getCust_phone()
                });
            }
        } catch (RemoteException ex) {
            Logger.getLogger(ShoeMgt.class.getName()).log(Level.SEVERE, null, ex);
        } catch (NotBoundException ex) {
            Logger.getLogger(ShoeMgt.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

