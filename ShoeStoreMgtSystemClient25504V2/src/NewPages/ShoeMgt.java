    private void searchBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchBtnActionPerformed
        try {
        if (searchtxt.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please Enter Id ", "Empty Field", JOptionPane.ERROR_MESSAGE);
        }
       // else if(searchtxt.getText().length() != 5){
       //     JOptionPane.showMessageDialog(this, "Id must be 5 characters ", "Length of Id Error", JOptionPane.ERROR_MESSAGE);
       // }
        else {
            // Assuming Employee is your model class
            Shoe shoe = new Shoe();
             Registry theregistry = LocateRegistry.getRegistry("127.0.0.1", 6000);
            
            ShoeService service = (ShoeService) theregistry.lookup("shoe");

            shoe.setShoeId(Integer.parseInt(searchtxt.getText()));
            Shoe shoeObj = service.searchShoe(shoe);
            
            if (shoeObj == null) {
                JOptionPane.showMessageDialog(this, "Id does not Exist", "Result", JOptionPane.INFORMATION_MESSAGE);
            } else {
                // Populate data from the model to the text fields
                
                nametxt.setText(shoeObj.getShoeName());
                brandtxt.setText(shoeObj.getBrand());
             
                sizetxt.setText(""+shoeObj.getShoeSize());
                pricetxt.setText(""+shoeObj.getPrice());
                
                
                registerBtn.setEnabled(false);
            }
        }
    } catch (Exception ex) {
        ex.printStackTrace();
    }                // TODO add your handling code here:
    }//GEN-LAST:event_searchBtnActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
                
        int selectedRow = ShoeTable.getSelectedRow();
        DefaultTableModel tbModel = (DefaultTableModel) ShoeTable.getModel();
        int theId = Integer.parseInt(tbModel.getValueAt(selectedRow,0).toString());
        
        Shoe sh = new Shoe();
        
        sh.setShoeId(theId);
        
        sh.setShoeName(nametxt.getText());
        sh.setBrand(brandtxt.getText());
        sh.setShoeSize(Integer.parseInt(sizetxt.getText()));
        sh.setPrice(Double.parseDouble(pricetxt.getText()));
        
        try {
            Registry theregistry = LocateRegistry.getRegistry("127.0.0.1", 6000);
            
            ShoeService service = (ShoeService) theregistry.lookup("shoe");

//            shoe.setShoeId(Integer.parseInt(searchtxt.getText()));
            Shoe shoeObj = service.updateShoe(sh);
            if (shoeObj != null){
            JOptionPane.showMessageDialog(this, "shoe updated");
            addTableRowDataShoe();
            nametxt.setText("");
            brandtxt.setText("");
            sizetxt.setText("");
            pricetxt.setText("");
            }else{
            JOptionPane.showMessageDialog(this, "shoe not updated");
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
        
    }//GEN-LAST:event_jButton2ActionPerformed

